        <p>There are different types of coverage available, and you can compare travel insurance and choose the one that suits best for your specific needs. Here are some of the most popular ones available in India. </p>
        <ul class="angle-double-right">
<li>Domestic travel insurance: Any Indian resident and a non-resident can buy this insurance to safeguard against medical emergencies, permanent disability and death, checked-in lost/stolen baggage, travel delay/cancellation, emergency cash and personal liability during travel.</li>
<li>International travel insurance: This policy over and above the regular usually includes coverage for overseas medical expenses, baggage &amp; travel delays, hijack, evacuation to India and loss of passport or travel documents.</li>
<li>Corporate travel insurance: An employer or organization can get coverage for its employees for both international &amp; domestic travel, under corporate travel insurance in India. Generally the benefits included are Personal Belongings cover, Expenses due to cancelled flights, Expenses due to illness/injury, Stolen/lost money/personal belongings etc.</li>
<li>Student travel insurance: Parents of students who are planning to study abroad have an endless list of things to look into. One important aspect is coverage of medical expenses. All universities require that students have Insurance that would cover emergency situations for the duration of his/her course. Generally the following are included under student travel insurance : Equipment insurance (mobile, computer, etc), Personal liability, Medical expenses, etc.</li>
<li>Senior citizen travel insurance: People in the age group 61-70 can avail of this insurance. Over and above the general inclusions, iIt includes cashless hospitalization, dental treatments etc.</li>
<li>Family travel insurance: Family travel insurance covers hospitalization, baggage loss, and other incidental expenses. The claim disbursement is easy with minimal paperwork involved.</li>
<li>Individual travel insurance: This policy safeguards you against trip cancellation, home burglary etc.</li>
</ul>
